// TestKernelComparison generates side-by-side PNG comparisons of kernel variants.
//
//	go test ./docs/gen/ -run TestKernelComparison -v
func TestKernelComparison(t *testing.T) {
	if testing.Short() {
		t.Skip("skipped in short mode: generates docs/img/kernel/*.png")
	}

	outDir := filepath.Join("..", "img", "kernel")
	if err := os.MkdirAll(outDir, 0o755); err != nil {
		t.Fatalf("create output dir: %v", err)
	}

	const gap = 4

	quadratic := func(d, c float64) float64 { tf := 1 - d/c; return tf * tf }
	linear := func(d, c float64) float64 { return 1 - d/c }
	gaussian := func(d, c float64) float64 { s := c / 3; return math.Exp(-d * d / (2 * s * s)) }

	aggMax := func(cur, cand float64) float64 { return math.Max(cur, cand) }
	aggSum := func(cur, cand float64) float64 { return cur + cand }

	pow4 := func(s float64) float64 { return math.Pow(s, 4) }
	pow := func(e float64) func(float64) float64 {
		return func(s float64) float64 { return math.Pow(s, e) }
	}

	save := func(name string, img image.Image) { writeVariantPNG(t, outDir, name, img) }

	p := heatmap.Params{ResolutionM: 75, SigmaM: 300}

	cv := func(features []geodata.Feature, sw func(float64) float64, kern func(float64, float64) float64, ag func(float64, float64) float64) *heatmap.Grid {
		return computeVariant(features, kMinLon, kMinLat, kMaxLon, kMaxLat, p, sw, kern, ag)
	}

	// ── Aggregation: max vs sum ───────────────────────────────────────────────
	{
		features := complexFeatures()
		gridMax := cv(features, pow4, quadratic, aggMax)
		gridSum := cv(features, pow4, quadratic, aggSum)

		sharedCeil := ceiling95(gridMax)

		imgMax := variantImageC(gridMax, sharedCeil, 2)
		imgSum := variantImageC(gridSum, sharedCeil, 2)
		save("agg_max.png", imgMax)
		save("agg_sum.png", imgSum)
		save("agg_compare.png", compositeH([]image.Image{imgMax, imgSum}, gap))
	}

	// ── Kernel shape: quadratic vs linear vs gaussian (real OSM data) ────────
	{
		features := richmondFeatures()
		pp := heatmap.Params{ResolutionM: 20, SigmaM: 150}
		cvRc := func(kern func(float64, float64) float64) *heatmap.Grid {
			return computeVariant(features, rcMinLon, rcMinLat, rcMaxLon, rcMaxLat, pp, pow4, kern, aggMax)
		}
		gQuad, gLinear, gGauss := cvRc(quadratic), cvRc(linear), cvRc(gaussian)
		ceilQ := ceiling95(gQuad)
		imgQuad := renderWithContext(gQuad, ceilQ, 3, rcRoads, rcPaths)
		imgLinear := renderWithContext(gLinear, ceiling95(gLinear), 3, rcRoads, rcPaths)
		imgGauss := renderWithContext(gGauss, ceiling95(gGauss), 3, rcRoads, rcPaths)
		save("kernel_quadratic.png", imgQuad)
		save("kernel_linear.png", imgLinear)
		save("kernel_gaussian.png", imgGauss)
		save("kernel_compare.png", compositeH([]image.Image{imgQuad, imgLinear, imgGauss}, gap))
	}

	// ── Zone of influence: σ=50 m vs σ=150 m vs σ=400 m (real OSM data) ────
	{
		features := richmondFeatures()
		sigmas := []struct {
			name  string
			sigma float64
		}{
			{"sigma_50", 50},
			{"sigma_150", 150},
			{"sigma_400", 400},
		}
		imgs := make([]image.Image, len(sigmas))
		for i, cfg := range sigmas {
			pp := heatmap.Params{ResolutionM: 20, SigmaM: cfg.sigma}
			g := computeVariant(features, rcMinLon, rcMinLat, rcMaxLon, rcMaxLat, pp, pow4, quadratic, aggMax)
			imgs[i] = renderWithContext(g, ceiling95(g), 3, rcRoads, rcPaths)
			save(cfg.name+".png", imgs[i])
		}
		save("sigma_compare.png", compositeH(imgs, gap))
	}

	// ── Similarity weight: sim^1 vs sim^2 vs sim^4 (real OSM data) ─────────
	{
		features := richmondFeatures()
		pp := heatmap.Params{ResolutionM: 20, SigmaM: 150}
		cvRcW := func(sw func(float64) float64) *heatmap.Grid {
			return computeVariant(features, rcMinLon, rcMinLat, rcMaxLon, rcMaxLat, pp, sw, quadratic, aggMax)
		}
		gSim1, gSim2, gSim4 := cvRcW(pow(1)), cvRcW(pow(2)), cvRcW(pow(4))
		imgSim1 := renderWithContext(gSim1, ceiling95(gSim1), 3, rcRoads, rcPaths)
		imgSim2 := renderWithContext(gSim2, ceiling95(gSim2), 3, rcRoads, rcPaths)
		imgSim4 := renderWithContext(gSim4, ceiling95(gSim4), 3, rcRoads, rcPaths)
		save("weight_sim1.png", imgSim1)
		save("weight_sim2.png", imgSim2)
		save("weight_sim4.png", imgSim4)
		save("weight_compare.png", compositeH([]image.Image{imgSim1, imgSim2, imgSim4}, gap))
	}

	t.Logf("wrote kernel comparison images → %s", outDir)
}
