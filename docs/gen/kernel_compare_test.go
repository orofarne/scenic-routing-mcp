// ── Rendering helpers ─────────────────────────────────────────────────────────

func absInt(x int) int {
	if x < 0 {
		return -x
	}
	return x
}

func clampInt(v, lo, hi int) int {
	if v < lo {
		return lo
	}
	if v > hi {
		return hi
	}
	return v
}

func ceiling95(g *heatmap.Grid) float64 {
	var vals []float64
	for _, row := range g.Values {
		for _, v := range row {
			if v > 0 {
				vals = append(vals, v)
			}
		}
	}
	if len(vals) == 0 {
		return 0
	}
	sort.Float64s(vals)
	c := vals[int(float64(len(vals))*0.95)]
	if c <= 0 {
		c = vals[len(vals)-1]
	}
	return c
}

func variantImageC(g *heatmap.Grid, ceiling, scale float64) image.Image {
	w := int(math.Round(float64(g.Width) * scale))
	h := int(math.Round(float64(g.Height) * scale))
	img := image.NewRGBA(image.Rect(0, 0, w, h))
	scaleI := int(math.Round(scale))

	for y := 0; y < g.Height; y++ {
		for x := 0; x < g.Width; x++ {
			t := 0.0
			if ceiling > 0 && g.Values[y][x] > 0 {
				t = math.Min(g.Values[y][x]/ceiling, 1.0)
				t = math.Pow(t, 0.4)
			}
			c := cmpHeatColor(t)
			for dy := 0; dy < scaleI; dy++ {
				for dx := 0; dx < scaleI; dx++ {
					img.SetRGBA(x*scaleI+dx, y*scaleI+dy, c)
				}
			}
		}
	}
	return img
}


func renderWithContext(g *heatmap.Grid, ceiling float64, scale int, roads, paths [][][2]float64) image.Image {
	w, h := g.Width*scale, g.Height*scale
	img := image.NewRGBA(image.Rect(0, 0, w, h))

	for y := 0; y < g.Height; y++ {
		for x := 0; x < g.Width; x++ {
			t := 0.0
			if ceiling > 0 && g.Values[y][x] > 0 {
				t = math.Min(g.Values[y][x]/ceiling, 1.0)
				t = math.Pow(t, 0.4)
			}
			c := cmpHeatColor(t)
			for dy := 0; dy < scale; dy++ {
				for dx := 0; dx < scale; dx++ {
					img.SetRGBA(x*scale+dx, y*scale+dy, c)
				}
			}
		}
	}

	toXY := func(lon, lat float64) (int, int) {
		return clampInt(int((lon-g.MinLon)/g.CellLon)*scale, 0, w-1),
			clampInt(int((g.MaxLat-lat)/g.CellLat)*scale, 0, h-1)
	}

	seg := func(x0, y0, x1, y1 int, c color.RGBA) {
		dx, dy, sx, sy := absInt(x1-x0), absInt(y1-y0), 1, 1
		if x0 > x1 {
			sx = -1
		}
		if y0 > y1 {
			sy = -1
		}
		err := dx - dy
		for {
			if x0 >= 0 && x0 < w && y0 >= 0 && y0 < h {
				img.SetRGBA(x0, y0, c)
			}
			if x0 == x1 && y0 == y1 {
				break
			}
			if e2 := 2 * err; e2 > -dy {
				err -= dy
				x0 += sx
			} else if e2 < dx {
				err += dx
				y0 += sy
			}
		}
	}

	pathC := color.RGBA{160, 160, 160, 255}
	for _, way := range paths {
		for i := 1; i < len(way); i++ {
			x0, y0 := toXY(way[i-1][0], way[i-1][1])
			x1, y1 := toXY(way[i][0], way[i][1])
			seg(x0, y0, x1, y1, pathC)
		}
	}

	roadC := color.RGBA{50, 50, 50, 255}
	for _, way := range roads {
		for i := 1; i < len(way); i++ {
			x0, y0 := toXY(way[i-1][0], way[i-1][1])
			x1, y1 := toXY(way[i][0], way[i][1])
			seg(x0, y0, x1, y1, roadC)
			seg(x0+1, y0, x1+1, y1, roadC)
		}
	}
	return img
}

func compositeH(imgs []image.Image, gap int) image.Image {
	if len(imgs) == 0 {
		return nil
	}
	h := imgs[0].Bounds().Dy()
	totalW := gap * (len(imgs) - 1)
	for _, img := range imgs {
		totalW += img.Bounds().Dx()
	}
	out := image.NewRGBA(image.Rect(0, 0, totalW, h))
	for y := 0; y < h; y++ {
		for x := 0; x < totalW; x++ {
			out.SetRGBA(x, y, color.RGBA{255, 255, 255, 255})
		}
	}
	ox := 0
	for i, src := range imgs {
		b := src.Bounds()
		for py := 0; py < b.Dy(); py++ {
			for px := 0; px < b.Dx(); px++ {
				out.Set(ox+px, py, src.At(b.Min.X+px, b.Min.Y+py))
			}
		}
		ox += b.Dx()
		if i < len(imgs)-1 {
			for py := 0; py < h; py++ {
				for gx := 0; gx < gap; gx++ {
					out.SetRGBA(ox+gx, py, color.RGBA{180, 180, 180, 255})
				}
			}
			ox += gap
		}
	}
	return out
}

func writeVariantPNG(t *testing.T, outDir, name string, img image.Image) {
	t.Helper()
	f, err := os.Create(filepath.Join(outDir, name))
	if err != nil {
		t.Errorf("create %s: %v", name, err)
		return
	}
	defer f.Close()
	if err := png.Encode(f, img); err != nil {
		t.Errorf("encode %s: %v", name, err)
	}
}

